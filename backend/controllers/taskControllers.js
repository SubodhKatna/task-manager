const Task = require("../models/Task");

// @desc    Get all tasks (Admin: all, User: only assigned tasks)
// @route   GET /api/tasks/
// @access  Private
const getTasks = async (req, res) => {
  try {
    const { status } = req.query;

    // baseQuery depends only on user role (assignedTo filter for non-admins)
    const baseQuery =
      req.user.role === "admin" ? {} : { assignedTo: req.user._id };

    // query used to fetch tasks (applies optional status filter if provided)
    const fetchQuery = { ...baseQuery, ...(status ? { status } : {}) };

    let tasks = await Task.find(fetchQuery).populate(
      "assignedTo",
      "name email profileImageUrl"
    );

    // Add completed todo checklist count to each task
    tasks = await Promise.all(
      tasks.map(async (task) => {
        const taskObj = task.toObject(); // safer than accessing _doc directly
        const todoChecklist = Array.isArray(taskObj.todoChecklist)
          ? taskObj.todoChecklist
          : [];
        const completedTodoCount = todoChecklist.filter(
          (item) => item && item.completed
        ).length;
        return { ...taskObj, completedTodoCount };
      })
    );

    // --- OPTIMIZED COUNTS ---
    // Status summary counts (always computed against baseQuery so counts reflect user's scope)
    const [allTasks, pendingTasks, inProgressTasks, completedTasks] =
      await Promise.all([
        Task.countDocuments(baseQuery),
        Task.countDocuments({ ...baseQuery, status: "Pending" }),
        Task.countDocuments({ ...baseQuery, status: "In Progress" }),
        Task.countDocuments({ ...baseQuery, status: "Completed" }),
      ]);

    // --- FIXED RESPONSE ---
    // Send the response in the format the frontend expects (statusSummary)
    res.json({
      tasks,
      statusSummary: {
        all: allTasks,
        pendingTasks: pendingTasks,
        inProgressTasks: inProgressTasks,
        completedTasks: completedTasks,
      },
    });
  } catch (error) {
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

// @desc    Get task by ID
// @route   GET /api/tasks/:id
// @access  Private
const getTaskById = async (req, res) => {
  try {
    const task = await Task.findById(req.params.id).populate(
      "assignedTo",
      "name email profileImageUrl"
    );

    if (!task) return res.status(404).json({ message: "Task not found" });

    res.json(task);
  } catch (error) {
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

// @desc    Create a new task (Admin only)
// @route   POST /api/tasks/
// @access  Private (Admin)
const createTask = async (req, res) => {
  try {
    const {
      title,
      description,
      priority,
      dueDate,
      assignedTo,
      attachments,
      todoChecklist,
    } = req.body;

    if (!Array.isArray(assignedTo)) {
      return res
        .status(400)
        .json({ message: "assignedTo must be an array of user IDs" });
    }

    const task = await Task.create({
      title,
      description,
      priority,
      dueDate,
      assignedTo,
      createdBy: req.user._id,
      todoChecklist,
      attachments,
    });

    res.status(201).json({ message: "Task created successfully", task });
  } catch (error) {
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

// @desc    Update task details
// @route   PUT /api/tasks/:id
// @access  Private
const updateTask = async (req, res) => {
  try {
    const task = await Task.findById(req.params.id);

    if (!task) return res.status(4404).json({ message: "Task not found" });

    task.title = req.body.title || task.title;
    task.description = req.body.description || task.description;
    task.priority = req.body.priority || task.priority;
    task.dueDate = req.body.dueDate || task.dueDate;
    task.todoChecklist = req.body.todoChecklist || task.todoChecklist;
    task.attachments = req.body.attachments || task.attachments;

    if (req.body.assignedTo) {
      if (!Array.isArray(req.body.assignedTo)) {
        return res
          .status(400)
          .json({ message: "assignedTo must be an array of user IDs" });
      }
      task.assignedTo = req.body.assignedTo;
    }
    const updatedTask = await task.save();
    res.json({ message: "Task updated successfully", updatedTask });
  } catch (error) {
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

